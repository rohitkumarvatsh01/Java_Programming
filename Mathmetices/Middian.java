import java.util.Scanner;
public class Middian{
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);

        System.out.println("Enter the Number:- ");
        int n=sc.nextInt();
        
        if(n%2==0){
            System.out.println("Middian of Number is:- "+(n/2));
        }
        else{
            System.out.println("Middian of Number is:- "+(n/2));
        }
    }
}